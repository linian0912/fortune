def get_shi_chen_from_hour(hour):
    h = hour % 24
    if h == 23 or h == 0: return '子'
    if 1 <= h <= 2: return '丑'
    if 3 <= h <= 4: return '寅'
    if 5 <= h <= 6: return '卯'
    if 7 <= h <= 8: return '辰'
    if 9 <= h <= 10: return '巳'
    if 11 <= h <= 12: return '午'
    if 13 <= h <= 14: return '未'
    if 15 <= h <= 16: return '申'
    if 17 <= h <= 18: return '酉'
    if 19 <= h <= 20: return '戌'
    return '亥'

