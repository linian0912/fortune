def get_xun_kong(jia_zi_index):
    """根据六十甲子序号获取空亡地支。"""
    xun_start = (jia_zi_index // 10) * 10
    return XUN_KONG.get(xun_start, ['?', '?'])


def compute_shen_sha(day_gan, year_zhi, day_zhi, pillars_zhi):
    """计算命局神煞（完整版20+种）。"""
    result = {}
    # 日干查
    tian_yi = TIAN_YI_GUI_REN.get(day_gan, [])
    if any(z in pillars_zhi for z in tian_yi):
        result['天乙贵人'] = [z for z in pillars_zhi if z in tian_yi]
    wc = WEN_CHANG.get(day_gan)
    if wc and wc in pillars_zhi: result['文昌'] = [wc]
    yr = YANG_REN.get(day_gan)
    if yr and yr in pillars_zhi: result['羊刃'] = [yr]
    ls = LU_SHEN_MAP.get(day_gan)
    if ls and ls in pillars_zhi: result['禄神'] = [ls]
    jy = JIN_YU_MAP.get(day_gan)
    if jy and jy in pillars_zhi: result['金舆'] = [jy]
    xt = XUE_TANG_MAP.get(day_gan)
    if xt and xt in pillars_zhi: result['学堂'] = [xt]
    # 太极贵人
    tj_list = TAI_JI_MAP.get(day_gan, [])
    found_tj = [z for z in pillars_zhi if z in tj_list]
    if found_tj: result['太极贵人'] = found_tj
    fx = FU_XING_MAP.get(day_gan)
    if fx and fx in pillars_zhi: result['福星贵人'] = [fx]
    # 年支查
    for name, m in [('桃花',TAO_HUA_MAP),('驿马',YI_MA_MAP),('华盖',HUA_GAI_MAP),
                     ('将星',JIANG_XING_MAP),('红鸾',HONG_LUAN_MAP),('天喜',TIAN_XI_MAP),
                     ('孤辰',GU_CHEN_MAP),('寡宿',GUA_SU_MAP),('劫煞',JIE_SHA_MAP),
                     ('灾煞',ZAI_SHA_MAP),('金匮',JIN_KUI_MAP)]:
        s = m.get(year_zhi)
        if s and s in pillars_zhi: result[name] = [s]
    # 月支查（需要month_zhi参数，这里先只加天德/月德到bazi层面）
    return result


